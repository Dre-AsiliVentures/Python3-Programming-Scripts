# Creating Dictonary

#Method 1: Direct Method
family={'Dad':'Mwema','Mum':'Nina','Bro_Syz':'Tara','Size':9}
print ("Method 1: Direct:\n\n      {}\n".format(family))
#Method 2: Using Dict ()
family=dict(Dad='Mwema',Mum='Nina',Bro_Syz='Tara',size=9)
print ("Method 2: Using Dict(): \n\n      {}\n".format(family))
#Method 3: Using Lists
family=[('Dad','Mwema'),('Mum','Nina'),('Bro_Syz','Tara'),('Size',9)]
print ("Method 1: Using Lists: \n\n      {}\n".format(family))
